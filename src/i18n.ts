export function languageFromHass(hass: HomeAssistant | undefined): TranslationLanguage {
  const language = (hass?.locale?.language ?? hass?.language ?? "da").toLowerCase();
  return language.startsWith("en") ? "en" : "da";
}

export function translate(language: TranslationLanguage, key: keyof typeof translations.da, values: Record<string, string> = {}): string {
  const template = translations[language][key] ?? translations.da[key] ?? key;
  return Object.entries(values).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, value), template);
}
